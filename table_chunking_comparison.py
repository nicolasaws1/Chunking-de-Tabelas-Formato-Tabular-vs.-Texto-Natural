# Funções idênticas ao pipeline v2
def gerar_vetores_densos_batch(textos: list, batch_size: int = 8) -> list:
    embeddings = dense_model.encode(
        textos, batch_size=batch_size,
        normalize_embeddings=True, show_progress_bar=True
    )
    return [emb.tolist() for emb in embeddings]


def gerar_vetor_esparso(texto: str) -> dict:
    tokens = sparse_tokenizer(
        texto, return_tensors='pt',
        max_length=512, truncation=True, padding='max_length'
    )
    with torch.no_grad():
        output    = sparse_model(**tokens)
        ativacoes = torch.relu(output.logits)
        max_at, _ = ativacoes.max(dim=1)
        max_at    = max_at.squeeze(0)
        indices   = (max_at > 0).nonzero(as_tuple=True)[0].tolist()
        valores   = max_at[indices].tolist()
    if not indices:
        indices, valores = [0], [0.0]
    return {'indices': indices, 'values': valores}


