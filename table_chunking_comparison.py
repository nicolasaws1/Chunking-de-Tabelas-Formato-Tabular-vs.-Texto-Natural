"""---
## 9. Índices FAISS e Funções de Busca

Dense: Inner Product com vetores normalizados (= cosine, igual ao Qdrant Cloud).
Sparse: dot product manual sobre vocabulário ativo (replica Qdrant sparse).
"""

def build_dense_index(chunks: list) -> faiss.IndexFlatIP:
    vecs = np.array([c['dense_vec'] for c in chunks], dtype='float32')
    idx  = faiss.IndexFlatIP(vecs.shape[1])
    idx.add(vecs)
    return idx


def sparse_dot(q_sv: dict, c_sv: dict) -> float:
    q_map = dict(zip(q_sv['indices'], q_sv['values']))
    return sum(q_map.get(i, 0.0) * v for i, v in zip(c_sv['indices'], c_sv['values']))


def buscar_dense(query: str, faiss_idx, chunks: list, k: int = 3) -> list:
    q_emb = dense_model.encode([query], normalize_embeddings=True).astype('float32')
    scores, idxs = faiss_idx.search(q_emb, min(k, len(chunks)))
    return [{'rank': r+1, 'score': round(float(scores[0][r]),4),
             'chunk_id': chunks[idxs[0][r]]['chunk_id'],
             'text': chunks[idxs[0][r]]['text']}
            for r in range(len(idxs[0])) if idxs[0][r] < len(chunks)]


def buscar_sparse(query: str, chunks: list, k: int = 3) -> list:
    q_sv   = gerar_vetor_esparso(query)
    scored = sorted([(sparse_dot(q_sv, c['sparse_vec']), i)
                     for i, c in enumerate(chunks)], reverse=True)
    return [{'rank': r+1, 'score': round(scored[r][0],4),
             'chunk_id': chunks[scored[r][1]]['chunk_id'],
             'text': chunks[scored[r][1]]['text']}
            for r in range(min(k, len(scored)))]


