"""---
## 6. Estratégia B — Texto Natural (por linha)

Cada linha → `"Col1: val | Col2: val | ..."` com cabeçalho embutido.
Compatível com `tipo: 'texto'` do pipeline v2.
"""

def gerar_chunks_texto(tabelas: list, source: str) -> list:
    chunks = []
    for t in tabelas:
        header = t['header']
        for row_i, row in enumerate(t['rows']):
            pares = []
            for col_i, col_name in enumerate(header):
                val = row[col_i].strip() if col_i < len(row) else ''
                if val:
                    pares.append(f'{col_name}: {val}')
            if not pares:
                continue
            texto = ' | '.join(pares)
            chunks.append({
                'chunk_id':  f'text_p{t["page_no"]}_t{t["table_num"]}_r{row_i+1}',
                'strategy':  'text_natural',
                'page':      t['page_no'],
                'table_num': t['table_num'],
                'row_num':   row_i + 1,
                'source':    source,
                'text':      texto,
                'chars':     len(texto),
                'tipo':      'texto',
            })
    return chunks


