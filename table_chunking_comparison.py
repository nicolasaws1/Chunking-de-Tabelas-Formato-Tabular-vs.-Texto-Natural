def converter_pdf_bytes_local(pdf_path: str):
    """
    Lê o PDF como bytes e converte via tempfile.
    Padrão idêntico ao converter_pdf_bytes() do pipeline v2.
    """
    # 1. Ler bytes
    with open(pdf_path, 'rb') as f:
        pdf_bytes = f.read()
    print(f'  PDF lido: {len(pdf_bytes)/1024:.1f} KB')

    # 2. Configuração idêntica ao pipeline v2
    opts = PdfPipelineOptions()
    opts.do_ocr             = True
    opts.do_table_structure = True
    opts.table_structure_options.do_cell_matching = True
    opts.ocr_options = TesseractCliOcrOptions(
        lang=['por', 'eng'], force_full_page_ocr=True
    )
    converter = DocumentConverter(
        format_options={InputFormat.PDF: PdfFormatOption(pipeline_options=opts)}
    )

    # 3. Gravar em tempfile e converter
    with tempfile.NamedTemporaryFile(suffix='.pdf', delete=False) as tmp:
        tmp.write(pdf_bytes)
        tmp_path = tmp.name
    try:
        return converter.convert(tmp_path).document
    finally:
        try:
            os.remove(tmp_path)
        except OSError:
            pass


def extrair_tabelas_pagina(doc, target_page: int) -> list:
    """
    Percorre doc.iterate_items(), filtra TableItems da pagina alvo.
    Extrai grid via element.data.grid; fallback para parse de Markdown.
    """
    tabelas, t_counter = [], 0

    for element, _level in doc.iterate_items():
        if not isinstance(element, TableItem):
            continue

        page_no = None
        if hasattr(element, 'prov') and element.prov:
            page_no = element.prov[0].page_no
        if page_no != target_page:
            continue

        t_counter += 1

        try:
            grid = element.data.grid
            raw  = [[cell.text.strip() if cell.text else '' for cell in row]
                    for row in grid]
        except AttributeError:
            # Fallback: parsear Markdown exportado pelo Docling
            md    = element.export_to_markdown() if hasattr(element, 'export_to_markdown') else ''
            lines = [ln.strip() for ln in md.splitlines() if ln.strip().startswith('|')]
            raw   = [[c.strip() for c in ln.strip('|').split('|')] for ln in lines]
            raw   = [r for r in raw if not all(set(c) <= set('-: ') for c in r)]

        header = raw[0] if raw else []
        rows   = raw[1:] if len(raw) > 1 else []

        tabelas.append({
            'table_num': t_counter,
            'page_no':   page_no,
            'header':    header,
            'rows':      rows,
        })

    return tabelas


